# If the previous conditions were not true, try this condition


# What happens when Python finds a true condition in an if-elif chain?
# It executes that block and skips the rest


# How many elif statements can you have?
# As many as you need


# Print "1" if a is equal to b, print "2" if a is greater than b, otherwise print "3".
a = 50
b = 10
if a == b:
  print("1")
elif a > b:
  print("2")
else:
  print("3")
